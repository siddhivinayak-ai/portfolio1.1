const gallery = document.getElementById("gallery");
const categoryButtons = document.querySelectorAll(".category-btn");
const modal = document.getElementById("modal");
const modalImage = document.getElementById("modalImage");
const closeModal = document.getElementById("closeModal");

function displayCertificates(filter) {
  gallery.innerHTML = "";
  const filteredCertificates = filter === "all" 
    ? certificates 
    : certificates.filter(cert => cert.category === filter);
    
  filteredCertificates.forEach(cert => {
    const card = document.createElement("div");
    card.classList.add("card");
    card.innerHTML = `
  <img class="cert-img" src="${cert.image}" alt="${cert.title}">
  <div class="info">
    <div class="company-info">
      <img class="company-logo" src="${cert.logo}" alt="${cert.company}">
      <span class="company-name">${cert.company}</span>
    </div>
    <h3>${cert.title}</h3>
    <p>${cert.description}</p>
    <button class="view-btn" data-image="${cert.image}">View</button>
  </div>
`;

    gallery.appendChild(card);
  });
}

gallery.addEventListener("click", (e) => {
  // Check if the clicked element is the "View" button or the certificate image
  if (e.target.classList.contains("view-btn") || e.target.classList.contains("cert-img")) {
    const image = e.target.getAttribute("data-image") || e.target.src; // Get image source
    modalImage.src = image; // Set the modal image
    modal.style.display = "flex"; // Show the modal
  }
});


closeModal.addEventListener("click", () => {
  modal.style.display = "none";
});

categoryButtons.forEach(button => {
  button.addEventListener("click", () => {
    const category = button.getAttribute("data-category");
    displayCertificates(category);
  });
});

// Display all certificates on load
displayCertificates("all");
